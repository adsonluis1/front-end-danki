// for in e for of


var num = [15 , 10 , 30 , 60]


console.log('resultado de for in :') // ele mostra o length do arrey
for(var o in num){
    console.log(o)
}

console.log('-------------------------------------------------------')

console.log('resultado de for of:') 

for(var o of num){ // ele mostra os elementos de dentro do arrey
    console.log(o)
}

